package Lec8_ObjectsAndClasses.MethodOverLoading.DataType;

public class ChangingDataTypeOfParameters {

    public static int multifly(int a, int b) {
        return a * b;
    }

    public static double add(double a, double b) {
        return a * b;
    }
}
